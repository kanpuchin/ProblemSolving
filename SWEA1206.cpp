#include <stdio.h>
#define MAX(a, b) (((a) > (b)) ? (a) : (b))

int main(){
    int arr[1000] = {0,};
    int length;
    freopen("input.txt","r",stdin);
    for(int i=1; i<=10; i++){
        scanf("%d ", &length);
        for(int j=0; j<length; j++)
            scanf("%d ", &arr[j]);
        int view = 0;
        int cmp, clear;
        for(int i=2; i<length-2; i++){
            cmp = MAX(MAX(arr[i-2],arr[i-1]),MAX(arr[i+1],arr[i+2]));
            clear = arr[i] - cmp;
            if(clear <= 0)
                continue;
            view += clear;
        }
        printf("#%d %d\n", i, view);
    }
    return 0;
}